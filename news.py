def broadcast_flex_message(access_token, flex_carousels):
    """
    ‡∏™‡πà‡∏á Flex Message ‡πÑ‡∏õ‡∏¢‡∏±‡∏á LINE Broadcast (‡∏Å‡∏£‡∏∞‡∏à‡∏≤‡∏¢‡πÉ‡∏´‡πâ‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡∏ó‡∏∏‡∏Å‡∏Ñ‡∏ô‡∏ó‡∏µ‡πà add bot)
    - ‡∏ñ‡πâ‡∏≤ DRY_RUN = True ‡∏à‡∏∞‡πÑ‡∏°‡πà‡∏¢‡∏¥‡∏á‡∏à‡∏£‡∏¥‡∏á ‡πÅ‡∏Ñ‡πà print ‡∏ï‡∏±‡∏ß payload ‡πÉ‡∏´‡πâ‡∏î‡∏π
    """
    url = 'https://api.line.me/v2/bot/message/broadcast'
    headers = {"Content-Type": "application/json", "Authorization": f"Bearer {access_token}"}
    for idx, carousel in enumerate(flex_carousels, 1):
        payload = {"messages": [carousel]}
        if DRY_RUN:
            # ‡πÇ‡∏´‡∏°‡∏î‡∏ó‡∏î‡∏•‡∏≠‡∏á: ‡πÅ‡∏™‡∏î‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ó‡∏µ‡πà ‚Äú‡∏à‡∏∞‚Äù ‡∏™‡πà‡∏á
            print(f"[DRY_RUN] Carousel #{idx}: {json.dumps(payload)[:500]}...")
            continue
        try:
            resp = S.post(url, headers=headers, json=payload, timeout=TIMEOUT)
            print(f"Broadcast #{idx} status:", resp.status_code, getattr(resp, "text", ""))
            if resp.status_code >= 300:
                # ‡∏ñ‡πâ‡∏≤‡∏™‡πà‡∏á‡πÅ‡∏•‡πâ‡∏ß error (‡πÄ‡∏ä‡πà‡∏ô 400/401/500) ‡πÉ‡∏´‡πâ‡∏´‡∏¢‡∏∏‡∏î‡πÑ‡∏°‡πà‡∏™‡πà‡∏á‡∏ï‡πà‡∏≠
                break
            time.sleep(1.2)  # ‡∏´‡∏ô‡πà‡∏ß‡∏á‡πÄ‡∏•‡πá‡∏Å‡∏ô‡πâ‡∏≠‡∏¢ ‡∏£‡∏∞‡∏´‡∏ß‡πà‡∏≤‡∏á‡∏Å‡∏≤‡∏£‡∏™‡πà‡∏á‡πÅ‡∏ï‡πà‡∏•‡∏∞ carousel
        except Exception as e:
            print("[LINE ERROR]", e)
            break

